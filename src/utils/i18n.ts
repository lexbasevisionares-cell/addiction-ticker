export function getCurrencySymbol(currency: Currency) {
  switch (currency) {
    case 'USD': return '$';
    case 'GBP': return '£';
    case 'EUR':
    default: return '€';
  }
}

export function formatCurrency(value: number, currency: Currency, language: Language) {
  const locale = language === 'fi' ? 'fi-FI' : 'en-US';
  return new Intl.NumberFormat(locale, { style: 'currency', currency: currency, minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(value);
}

